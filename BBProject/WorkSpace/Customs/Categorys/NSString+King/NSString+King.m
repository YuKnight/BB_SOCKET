@implementation NSString (King)

#pragma mark - 判断是不是都是空格或者空
+ (BOOL)isEmptyOrNull:(NSString *)string
{
    if (!string || ![string isKindOfClass:[NSString class]])
    { //空对象
        return YES;
    }
    else
    {
        NSString *tempString = [string stringByTrimmingCharactersInSet:[NSCharacterSet whitespaceAndNewlineCharacterSet]];
        if ([tempString length] == 0)
        { //空字符串
            return YES;
        }
        else
        { //不为空
            return NO;
        }
    }
}

#pragma mark - 获取设备的UUID
+ (NSString *)getUUID
{
    CFUUIDRef puuid = CFUUIDCreate(nil);
    CFStringRef uuidString = CFUUIDCreateString(nil, puuid);
    CFRelease(puuid);
    NSString *result = (NSString *)CFBridgingRelease(CFStringCreateCopy(NULL, uuidString));
    CFRelease(uuidString);
    
    return result;
}

#pragma mark - 获得一个字符串的 MD5 值
- (NSString *)getMD5String
{
    const char *cStr = [self UTF8String];
    unsigned char result[32];
    CC_MD5(cStr, (CC_LONG)strlen(cStr), result);
    NSString *md5String = [[NSString stringWithFormat:
                           @"%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X",
                           result[0],  result[1],  result[2],  result[3],
                           result[4],  result[5],  result[6],  result[7],
                           result[8],  result[9],  result[10], result[11],
                           result[12], result[13], result[14], result[15]] lowercaseString];
    
    return md5String;
}

#pragma mark - 密码加密处理(传阿波罗数据时需要这样加密下)
- (NSString *)passwordEncrypt
{
    NSString *password = [[NSString stringWithFormat:@"%@CK2012", [self lowercaseString]] getMD5String];
    return [password lowercaseString];
}

#pragma mark - 手机号码正则表达式匹配
+ (BOOL)checkPhotoNumber:(NSString *)photoNumber
{
    if ([photoNumber length] == 0) return NO;
    
    NSString *regex = @"^((13[0-9])|(147)|(15[^4,\\D])|(18[0,5-9]))\\d{8}$"; //正则表达式
    NSPredicate *predicate = [NSPredicate predicateWithFormat:@"SELF MATCHES %@", regex];
    BOOL isMatch = [predicate evaluateWithObject:photoNumber];
    if (!isMatch)
    {
        return NO;
    }

    return YES;
}

#pragma mark - 检测密码合法性（密码由6-16位数字或字母组成）
+ (BOOL)checkPassword:(NSString *)password
{
    // 匹配密码由6-16位数字或字母组成的字符串的正则表达式：
    NSString *regex = @"^[A-Za-z0-9]{6,16}$";
    NSPredicate *pred = [NSPredicate predicateWithFormat:@"SELF MATCHES %@", regex];
    BOOL isMatch = [pred evaluateWithObject:password];
    
    return isMatch;
}

#pragma mark - 限制string的长度max (汉字算2字符)
+ (BOOL)limitString:(NSString *)string max:(int)max
{
    //[\u4e00-\u9fa5] 匹配中文
    NSString *regex = @"^[\u4e00-\u9fa5A-Za-z0-9_\\s\\(\\)\\.\\-]{1,10}$";
    NSPredicate *pred = [NSPredicate predicateWithFormat:@"SELF MATCHES %@", regex];
    BOOL isMatch = [pred evaluateWithObject:string];
    
    if (isMatch && [string actualCharacterLength] > max)
    { //验证字符（什么一个汉字2个字符）
        isMatch = NO;
    }
    
    return isMatch;
}
- (int)actualCharacterLength
{ //计算实际的字符长度
    int n = (int)[self length], a = 0, b = 0, l = 0;
    unichar c;
    for(int i = 0; i < n; i++)
    {
        c = [self characterAtIndex:i];
        if (isblank(c))      b++;
        else if (isascii(c)) a++;
        else                 l++;
    }
    
    if(a == 0 && l == 0)
    {
        return 0;
    }
    
    return (a + b + (int)ceilf((float)l * 2.0f));
}

#pragma mark - 取名字的拼音首字母
- (NSString *)nameFirstLetter
{
    NSString *words = [self stringByTrimmingCharactersInSet:[NSCharacterSet whitespaceAndNewlineCharacterSet]];
    if (words.length == 0) return @"#";

    NSString *result = nil;
    unichar firstLetter = [words characterAtIndex:0];
    int index = firstLetter - HANZI_START;
    
    if (index >= 0 && index <= HANZI_COUNT)
    {
        result = [NSString stringWithFormat:@"%c", firstLetterArray[index]];
    }
    else if ((firstLetter >= 'a' && firstLetter <= 'z') ||
             (firstLetter >= 'A' && firstLetter <= 'Z'))
    {
        result = [NSString stringWithFormat:@"%c", firstLetter];
    }
    else
    {
        result = @"#";
    }
    
    return [result uppercaseString]; //转大写
//    return [result lowercaseString]; //转小写
}

#pragma mark - 将名字转成全拼（如"张三"转换后为"zhangsan"）
- (NSString *)nameToPinyin
{
    NSMutableString *pinyin = [self mutableCopy];
    CFStringTransform((__bridge CFMutableStringRef)pinyin, NULL, kCFStringTransformMandarinLatin, NO);
    CFStringTransform((__bridge CFMutableStringRef)pinyin, NULL, kCFStringTransformStripDiacritics, NO);
    
    return [[pinyin lowercaseString] stringByReplacingOccurrencesOfString:@" " withString:@""]; //转小写并去除空格
}

#pragma mark - 将名字转成拼音且只取首字母（如"张三"转换后为"zs"）
- (NSString *)nameToPinYinFirstLetter
{
    NSString *pinYinFirstLetter = @"";
    for (int index = 0; index < self.length; index++)
    {
        NSString *string = [self substringWithRange:NSMakeRange(index, 1)]; //包含该索引的位置
        NSString *firstLetter = [string nameFirstLetter];
        if ([firstLetter isEqualToString:@"#"])
        {
            firstLetter = string;
        }
        pinYinFirstLetter = [pinYinFirstLetter stringByAppendingString:firstLetter];
    }
    
    return [pinYinFirstLetter lowercaseString]; //转小写
}

